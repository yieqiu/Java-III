    private void configurarTabela(){
        DefaultTableModel m = new DefaultTableModel(){
            @Override
            public boolean isCellEditable(int row, int column){
                return false;
            }  
        };
        
        //Definir as colunas
        m.addColumn("Nome");
        m.addColumn("Descrição");
        m.addColumn("Categoria");
        m.addColumn("Tipo");
        m.addColumn("Venda");
        tabImoveis.setModel(m);
    }
