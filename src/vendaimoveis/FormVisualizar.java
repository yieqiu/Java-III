    public FormVisualizar(ArrayList<Imovel> lista){
        this();
   
        for(Imovel c: lista){
            DefaultTableModel m = (DefaultTableModel)tabImoveis.getModel();
            m.addRow(
                new String[]{c.nome,c.desc,c.categ,c.tipo,c.venda}
            );
            tabImoveis.setModel(m);
        }
    }

